SELECT C.CLASS_NO, C.CLASS_NAME, TRUNC(AVG(G.POINT),8)
FROM TB_CLASS C
JOIN TB_GRADE G ON (C.CLASS_NO = G.CLASS_NO)
WHERE C.DEPARTMENT_NO = (SELECT DEPARTMENT_NO FROM TB_DEPARTMENT WHERE DEPARTMENT_NAME = '환경조경학과') AND 
    C.CLASS_TYPE LIKE '%전공%'
GROUP BY C.CLASS_NO, C.CLASS_NAME
ORDER BY 1;

