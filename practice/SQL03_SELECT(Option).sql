-- 3. 
SELECT STUDENT_NAME "학생이름",
       STUDENT_NO "학번",
       STUDENT_ADDRESS "거주지 주소"
FROM TB_STUDENT
WHERE STUDENT_NO LIKE '9%' AND ( STUDENT_ADDRESS LIKE '%강원도%' OR  STUDENT_ADDRESS LIKE '%경기도%')
ORDER BY "학생이름";

